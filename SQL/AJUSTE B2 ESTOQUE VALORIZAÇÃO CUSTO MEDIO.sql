SELECT SB1.B1_COD,
	   SB1.B1_DESC,
	   SB1.B1_UPRC,
 	   SB2.B2_QATU,
	   SB2.B2_VATU1,
	   SB2.B2_VATU2,
	   SB2.B2_CM1,
	   SB2.B2_CM2
FROM SB1040 SB1
INNER JOIN SB2040 SB2 ON SB1.B1_COD = SB2.B2_COD
WHERE SB1.B1_TIPO = 'PA'
AND SB1.D_E_L_E_T_ = ''
AND SB2.D_E_L_E_T_ = ''
AND SB1.B1_UPRC <> 0
AND SB2.B2_QATU <> 0
GO


