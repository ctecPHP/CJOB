SET DATEFORMAT ymd
SELECT SC5.C5_NUM,
       SC5.C5_ROMANEI,
	   SC5.C5_NOTA,
	   SC5.C5_QTDPED,
	   SUM(SC6.C6_QTDVEN) AS C6_QTDEVEN,
	   SUM(SD2.D2_QUANT) AS D2_QUANT,
	   SC5.C5_EMISSAO,
	   SF2.F2_CHVNFE,
	   SF2.F2_EMISSAO,
	   DATEDIFF(DAY, SC5.C5_EMISSAO, SF2.F2_EMISSAO) AS DIAS	   	     	   
FROM SC5040 SC5
INNER JOIN SC6040 SC6 ON (SC6.C6_NUM = SC5.C5_NUM AND SC6.D_E_L_E_T_  = '') 
INNER JOIN SD2040 SD2 ON (SD2.D2_DOC = SC5.C5_NOTA AND SD2.D_E_L_E_T_ = '')
INNER JOIN SF2040 SF2 ON (SF2.F2_DOC = SD2.D2_DOC  AND SF2.D_E_L_E_T_ = '')
WHERE SC5.C5_EMISSAO BETWEEN '20180801' AND GETDATE()
AND SC5.C5_NOTA <> ''
AND SC5.D_E_L_E_T_ = ''
AND SC5.C5_ZZTIPO <> 'L'
AND SC5.C5_ROMANEI <> ''
AND SC5.C5_STAPED = 'F'
GROUP BY SC5.C5_NUM,
         SC5.C5_ROMANEI,
		 SC5.C5_NOTA,
         SC5.C5_QTDPED,
		 SC5.C5_EMISSAO,
		 SF2.F2_CHVNFE,
		 SF2.F2_EMISSAO	 
ORDER BY F2_EMISSAO ASC
GO